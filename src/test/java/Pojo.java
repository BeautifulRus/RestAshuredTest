public class Pojo {

        String name;
        String type;
        String exotic;



        public Pojo(){}

    public Pojo(String name, String type, String exotic) {
        this.name = name;
        this.type = type;
        this.exotic = exotic;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public String getExotic() {
        return exotic;
    }
}
